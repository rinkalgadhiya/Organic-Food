		<h1>About fruit and vegetables</h1>
		<p>Fruit and vegetables should be an important part of your daily diet. They are naturally good and contain vitamins and minerals that can help to keep you healthy. They can also help protect against some diseases.</p>

		<p>Most Australians will benefit from eating more fruit and vegetables as part of a well-balanced, healthy diet and an active lifestyle. There are many varieties of fruit and vegetables available and many ways to prepare, cook and serve them.</p>

		<p>Fruit and vegetables are best bought when they are in season. Otherwise try frozen or canned vegetables as they are just as nutritious and budget-friendly as well.</p>

		<p>You should eat at least 5 serves of vegetables and 2 serves of fruit each day. Choose different colours and varieties.</p>

		<p>If you are someone who doesn’t look forward to eating fruit or vegetables, start slowly with those you do like. Try serving, flavouring or cooking them in different ways. You can also disguise them in sauces, minced meals or curries.</p>

		<h1>Vitamins and minerals in fruit and vegetables</h1>
		<p>Fruits and vegetables contain many vitamins and minerals that are good for your health. Many of these are antioxidants, and may reduce the risk of many diseases:</p>

		<ul>
			<li>vitamin A (beta-carotene)</li>
			<li>vitamin C</li>
			<li>vitamin E</li>
			<li>magnesium</li>
			<li>zinc</li>
			<li>phosphorous</li>
			<li>folic acid.</li>
		</ul>
		
		<p>Folic acid may reduce blood levels of homocysteine, a substance that may be a risk factor for coronary heart disease.</p>

		<p>Research has shown that consuming these nutrients as food, within fruits and vegetables, is more beneficial for health than consuming them as supplements.</p>

		<h1>Fruit and vegetables for good health</h1>
		<p>Fruits and vegetables are low in fat, salt and sugar. They are a good source of dietary fibre, which can make you feel fuller for longer and prevent overconsumption of food. As part of a well-balanced, healthy diet and an active lifestyle, a high intake of fruit and vegetables can help you to:</p>

		<ul>
			<li>reduce obesity and maintain a healthy weight</li>
			<li>lower your cholesterol</li>
			<li>lower your blood pressure.</li>
		</ul>
		
		<h1>Fruit and vegetables and protection against diseases</h1>
		<p>Vegetables and fruit contain antioxidants and phytochemicals, or plant chemicals. These biologically active substances can help to protect you from some diseases.</p>

		<p>Scientific research shows that if you regularly eat lots of fruit and vegetables, you have a lower risk of:</p>

		<ul>
			<li>type 2 diabetes</li>
			<li>stroke</li>
			<li>heart (cardiovascular) disease</li>
			<li>cancer – some forms of cancer, especially bowel, stomach and throat cancers later in life</li>
			<li>high blood pressure (hypertension).</li>
		</ul>

