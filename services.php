		<p>Horticulture is the cultivation of plants in gardens or greenhouses, as opposed to the field-scale production of crops characteristic of agriculture. </p>

		<p>It includes the cultivation of fruits, vegetables, nuts, seeds, herbs, sprouts, mushrooms, algae, flowers, seaweeds and non-food crops such as grass and ornamental trees and plants. It also includes plant conservation, landscape restoration, landscape and garden design, construction, and maintenance, and arboriculture, ornamental trees and lawns.</p>

		<p>In anthropology, horticulture refers to a subsistence strategy characterized by the small-scale, non-industrial cultivation of plants for food. Horticulture involves the use of hand tools such as digging sticks, hoes and carrying baskets. In contrast to horticulture, agriculture is viewed by anthropologists as a more intensive strategy involving the use of plowing, animal traction and complex techniques of irrigation and soil management.</p>

		<p>The study and practice of horticulture have been traced back many years. Horticulture contributed to the transition from nomadic human communities to sedentary, or semi-sedentary, horticultural communities.</p> 

		<p>Horticulture is divided into several categories which focus on the cultivation and processing of different types of plants and food items for specific purposes. In order to conserve the science of horticulture, multiple organizations worldwide educate, encourage, and promote the advancement of horticulture. Some notable horticulturists include Luca Ghini and Luther Burbank.</p>

		<p>Horticulture, from Latin hortus meaning garden and colere meaning to cultivate, focuses on the use of small plots, in contrast to agronomy which involves intensive crop farming and large-scale field crop production of grains and forages or forestry involving forest trees and products derived from them.</p>

		<p>It deals with garden crops such as ornamental plants grown for their appearance, fruits and vegetable and spices grown for their food value, and medicinal plants.</p>

		<h1>Plant selection</h1>
		<p>When selecting plants to cultivate, a horticulturist may consider plant aspects based on their intended use and can include plant morphology, rarity, and utility.</p>

		<h1>Pre-plant bed preparation</h1>
		<p>Before planting, plant beds are weeded, extra mulch is removed, fertilizers or other soil enrichment is added, the bed is tilled, and the irrigation system is tested.</p>

		<h1>Adding plants</h1>
		<p>If transplanting plants from a pot to a soil bed, plants are planted at the same depth as the pot and are spaced as to not crowd the plants.</p>

		<h1>Pruning</h1>
		<p>Pruning has multiple functions. If growing shrubs, pruning overgrowth helps preserve the shape. Pruning can also increase the amount of flower buds on some species of flowering plants.</p>

		<h1>Mulching</h1>
		<p>Mulching is the process of applying a layer of mulch on top of the soil layer of a garden. Mulch is a natural weed suppressant, conserves moisture, and helps in moderating the soil temperature. There are different types of mulch and includes leaf and bark substrates.</p>

		<h1>Weed management</h1>
		<p>Weeds can be suppressed and managed through techniques such as mulching, selecting species that reduce weeds, and using cultural techniques.</p>

