<p class="p1">Consumer demand for organic food is growing at a rate of &nbsp;<span class="spanp"> 20 to 30% &nbsp;</span>per year. It is estimated that more than 6 out of every 10 Australian households now buy <br>organic foods on occasion. <br><br>Popular products include fruit and vegetables, beef, non-alcoholic <br>drinks, nuts and ready to eat foods.<br><br><br>

<label>Reasons to Buy Organic Food</label><br><br>
Most people buy organically-grown food products because they are concerned about pesticides, additives, antibiotics or other chemical residues, and believe organic food is healthier.

Other reasons include – concerns with the impact of conventional farming on <span class="spanp">biodiversity and the environment</span>, and the ethical treatment of livestock.</p>

<p class="p1"><label>Organic food and nutrition content</label><br><br>
Several studies have compared the nutritional content of organic and conventionally grown plants, and most have shown no significant differences in key <span class="spanp">vitamin and mineral</span> content. <br><br>

Organic foods promote more humane treatment of animals, as well as providing meat that is free from <span class="spanp">hormones and antibiotics</span>.</p>

<div class="p1">Although the differences are small – research has shown<br> that some organic food has:
<ul>
	<li>lower nitrate levels</li>
	<li>higher vitamin C levels</li>
	<li>higher levels of some minerals (such as phosphorous, magnesium and iron).</li>
</ul>
</div>

<p class="p1">Organic food is better for the environment Organic foods promote healthier and more sustainable use of natural resources. Modern conventional farming methods include excessive use of chemicals and planting only one crop multiple times (monocropping). This has led to a <span class="spanp">decline in soil fertility</span>, and an increase in <span class="spanp">salinity and blue-green algae </span>in waterways over many years.

Organic farmers try to minimise damage to the environment by diversifying their crops, using physical weed control, and animal and green manure.</p>

