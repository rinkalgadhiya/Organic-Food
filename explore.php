<p class="p1"><label>Organic food certification</label><br><br>
Organic farms are only certified after they have been operating according to organic principles for <span class="spanp">3 years</span>. However, the use of the word ‘organic’ is not regulated in Australia, so it is important to make sure that products you buy come from certified growers and producers.

Before 2009, a standard (guidelines and rules) did not exist for domestic and imported organic foods. This led to a misrepresentation of the word ‘organic’ in the Australian domestic food market.</p>

<p class="p1">Two key standards now govern the production, processing and labelling of organic food in Australia.<br><br> These are:<br>

The National Standard for Organic and Biodynamic Produce (for exported foods)<br>
The Australian Standard for Organic and Biodynamic Products (for domestic and imported foods).
</p><br><br>

