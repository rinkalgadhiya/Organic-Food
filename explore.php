<p class="p1">Organic farmers and food producers grow and produce food without using synthetic chemicals such as <span class="spanp">pesticides and artificial fertilisers</span>. They do not use genetically modified (GM) components or expose food to irradiation.

Animal welfare and environmental sustainability are important issues for organic farmers. The term <span class="spanp">‘organic’</span> can also cover animal products. For example, eggs certified as organic are free range, rather than from caged (battery) hens.

Organic farming Animals raised using organic methods are treated humanely and with respect. For example, chickens are free range and not kept in cages, and cows are not kept in feed lots. Animals are also not fed any growth-regulating drugs, steroids, hormones or antibiotics. However, the animals may be treated with vaccines to prevent disease.</p>

<p class="p1">Organic farming is also concerned with protecting the environment and working in harmony with existing ecosystems – including conserving water, soil and energy, and using renewable resources and natural farming cycles.<br><br>


Organic foods are not necessarily completely chemical free. They may be grown on land not previously used for organic food production and, therefore, might contain chemical residues. However, the <span class="spanp">pesticide residues </span>in organic food are considerably lower than those found in conventional farming – where foods are produced with <span class="spanp">synthetic chemicals</span>.</p>

