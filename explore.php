<p class="p1"><span class="spanp">‘Organic-certified produce’ </span>means the food was grown, harvested, stored and transported without the use of synthetic chemicals, irradiation or fumigants.<br><br>

<b>How to identify food certified as organic
Suggestions for making sure the food you are buying is organically grown include:</b><br><br>

If you are buying from an organic retailer – check for the Organic Retailers’ and Growers’ Association of Australia <span class="spanp">(ORGAA)</span> notice, which should be prominently displayed.
Choose foods with the label – ‘certified organic’ from one of the Department of Agriculture, Fisheries and Forestry accredited certifying organisations.
Check packaging for the grower’s name and certification number.
Do not be fooled by packaging that claims the produce is ‘natural’ or ‘chemical free’ if the proper certification labelling is not displayed.</p>
